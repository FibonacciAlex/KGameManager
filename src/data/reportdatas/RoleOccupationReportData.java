package data.reportdatas;

public class RoleOccupationReportData {
	
	public String occName;
	
	public String occCount;
	
	public String occRate;

	public String getOccName() {
		return occName;
	}

	public void setOccName(String occName) {
		this.occName = occName;
	}

	public String getOccCount() {
		return occCount;
	}

	public void setOccCount(String occCount) {
		this.occCount = occCount;
	}

	public String getOccRate() {
		return occRate;
	}

	public void setOccRate(String occRate) {
		this.occRate = occRate;
	}
	
	

}
