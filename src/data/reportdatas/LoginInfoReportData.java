package data.reportdatas;

public class LoginInfoReportData {
	
	public String reportDate;
	
	public String login1Count;
	
	public String login2Count;
	
	public String loginNCount;

	public String getReportDate() {
		return reportDate;
	}

	public void setReportDate(String reportDate) {
		this.reportDate = reportDate;
	}

	public String getLogin1Count() {
		return login1Count;
	}

	public void setLogin1Count(String login1Count) {
		this.login1Count = login1Count;
	}

	public String getLogin2Count() {
		return login2Count;
	}

	public void setLogin2Count(String login2Count) {
		this.login2Count = login2Count;
	}

	public String getLoginNCount() {
		return loginNCount;
	}

	public void setLoginNCount(String loginNCount) {
		this.loginNCount = loginNCount;
	}
	
	

}
