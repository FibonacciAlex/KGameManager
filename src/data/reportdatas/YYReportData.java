package data.reportdatas;

public class YYReportData {
	/**
	 * 日报表统计的日期
	 * 
	 * @return
	 */
	public String reportDate;
	
	public String createUserCount;
	
	public String channelCreateUserCount;
	
	public String todayChargeMoney;
	
	public String channelTodayChargeMoney;

	public String getReportDate() {
		return reportDate;
	}

	public void setReportDate(String reportDate) {
		this.reportDate = reportDate;
	}

	public String getCreateUserCount() {
		return createUserCount;
	}

	public void setCreateUserCount(String createUserCount) {
		this.createUserCount = createUserCount;
	}

	public String getChannelCreateUserCount() {
		return channelCreateUserCount;
	}

	public void setChannelCreateUserCount(String channelCreateUserCount) {
		this.channelCreateUserCount = channelCreateUserCount;
	}

	public String getTodayChargeMoney() {
		return todayChargeMoney;
	}

	public void setTodayChargeMoney(String todayChargeMoney) {
		this.todayChargeMoney = todayChargeMoney;
	}

	public String getChannelTodayChargeMoney() {
		return channelTodayChargeMoney;
	}

	public void setChannelTodayChargeMoney(String channelTodayChargeMoney) {
		this.channelTodayChargeMoney = channelTodayChargeMoney;
	}
	
	
}
